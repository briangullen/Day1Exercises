# Exercise 3 - Adding a number to a float
prefloat_number = int(input('Please provide a number: '))
postfloat_number = prefloat_number + .5
print(f"Your number plus 0.5 is {postfloat_number}")

# Exercise 4 -  Adding 2 floats
number_one = float(input('Please enter a number with a decimal: '))
number_two = float(input('Please enter another number with a decimal: '))
total_addition = number_one + number_two
print(f"Your numbers added together equal: {total_addition}")

# Exercise 5 - Multiplying floats
multiple_one = float(input('Please provide a number with a decimal: '))
multiple_two = float(input('Please provide another number with a decimal: '))
multiple_total = multiple_one * multiple_two
print(f"Your numbers multiplied equal: {multiple_total}")

# Exercise 6 - Dividing Integers
division_one = int(input('Please provide a whole number: '))
